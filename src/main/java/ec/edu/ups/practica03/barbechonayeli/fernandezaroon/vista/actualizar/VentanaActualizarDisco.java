    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
         String nombre= txtNombre.getText();
        
        String codigo=txtCodigo.getText();
       
       String años=txtAnios.getText();
       
        
         Disco disco = cantanteControlador.buscarporDisco(nombre);
         
        if (disco != null) { 
        txtCodigo.setText(disco.getCodigo()+"");
        txtAnios.setText(disco.getAnioDeLanzamiento()+"");
        
        
        disco.setNombreDisco(nombre);
        
        }else{
            JOptionPane.showMessageDialog(this, "La persona con la cedula"+nombre+"No fue econtrada");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        String codigo=txtCodigo.getText();
        String nombre=txtNombre.getText();
        String anioL=txtAnios.getText();
        
        int codigoUno=Integer.parseInt(codigo);
        int anioUno=Integer.parseInt(anioL);
        
        Disco disco=new Disco(codigoUno, nombre, anioUno);
        disco.setCodigo(codigoUno);
        disco.setNombreDisco(nombre);
        disco.setAnioDeLanzamiento(anioUno);
        
        if(cantanteControlador.actualizarDisco( disco)){
            JOptionPane.showMessageDialog(this, "El disco ha sido actualizado exitosamente! :)");
            this.limpiarCampos();   
            this.cambiarEstadoCampos(false);
        }else{
            JOptionPane.showMessageDialog(this, "El disco no ha sido actualizada! :(");
        }
    }//GEN-LAST:event_btnAceptarActionPerformed
private void cambiarEstadoCampos(boolean estado){
        this.txtCodigo.setEnabled(!estado);
        this.txtNombre.setEnabled(estado);
        this.txtAnios.setEnabled(estado);
        
        
    }
private void limpiarCampos(){
    this.txtCodigo.setText("");
    
    this.txtNombre.setText("");
    this.txtAnios.setText("");
   
    
}

