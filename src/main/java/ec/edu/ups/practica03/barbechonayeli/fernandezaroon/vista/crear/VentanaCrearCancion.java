    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String codigo= txtCodigo.getText();

        int codigoU=Integer.parseInt(codigo);

        Compositor compositor = compositorControlador.buscar(codigoU);

        if (compositor != null) {

            txtNombre.setText(compositor.getNombre());
            txtApellido.setText(compositor.getApellido());

            txtNacionalidad.setText(compositor.getNacionalidad());
            txtEdad.setText(compositor.getEdad()+"");

            txtNumeroComposiciones.setText(compositor.getNumeroDeComposiciones()+"");
            txtSalario.setText(compositor.getSalario()+"");
             compositor.setCodigo(codigoU);

        }else{
            JOptionPane.showMessageDialog(this, "La persona con la cedula"+codigoU+"No fue econtrada");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

