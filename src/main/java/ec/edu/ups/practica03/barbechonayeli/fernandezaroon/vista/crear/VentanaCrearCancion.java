    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        String codigo=txtCodigoCancion.getText();
       String titulo=txtTitulo.getText();
       String letra=txtLetra.getText();
       String tiempoMinutos=txtTiempo.getText();
       int codigoUno=Integer.parseInt(codigo);
       double tiempoUno=Double.parseDouble(tiempoMinutos);
       Cancion cancion = new Cancion(codigoUno, titulo, letra, tiempoUno);
                
               
                
                 if (codigoUno!=0) {
                
                
                cancion.setTitulo(titulo);
                cancion.setCodigo(codigoUno);
                cancion.setLetra(letra);
                cancion.setTiempoEnMinutos(tiempoUno);
               compositorControlador.crearCancion(cancion,compositorControlador.buscar(codigoUno));
               
                
                
                System.out.println(compositorControlador);
                JOptionPane.showMessageDialog(this, "Se a creado existosamente la cancion");
                this.limpiarCamposCompositor();
        }else{
            JOptionPane.showMessageDialog(this, "Ingrese un codigo");
        }
                
                this.limpiarCampos();
            
                
        
    }//GEN-LAST:event_btnAceptarActionPerformed
